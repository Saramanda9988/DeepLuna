package com.luna.deepluna.common.prompt;

public class Prompts {
    public static final String JUDGE_PROMPT = """
            请根据以下对话历史和当前用户问题，判断该问题是否足够清晰和具体，能够直接执行研究任务：
            
            当前用户问题：%s
        
            判断标准：
            1. 问题是否明确具体
            2. 研究范围是否清楚
            3. 期望的结果是否明确
            4. 是否包含足够的上下文信息
        
            回答要求（仅返回 JSON，不要添加其他说明）：
            - isClear：布尔值，true 或 false。
        
            示例：
        
            example1:
            {
                "isClear": true
            }
        
            example2:
            {
                "isClear": false,
            }
        
            你的回答必须只返回 JSON，不要返回任何额外文本或说明。
            """;

    // 生成澄清问题
    public static final String CLARIFY_PROMPT = """
            请根据对话历史和当前用户问题或者补充，生成1-2个澄清问题来帮助用户明确研究需求：
            
            当前用户问题或补充：%s
            
            请生成2-3个简洁明确的澄清问题，帮助用户提供更多必要信息。
            
            输出格式要求（仅返回 markdown内容，不要返回其他文字或说明）：
            - 内容：使用 Markdown 格式的字符串，包含编号或列表形式的澄清问题，多个问题用换行分隔。
            
            示例1（需要更多上下文）：
            
            - 1. 本次研究的主要目标或期望产出是什么？\\\\n- 2. 目标受众或使用场景有哪些？\\\\n- 3. 是否有时间或资源方面的限制？
            
            示例2（需确认输出形式）：
            
            - 1. 期望最终交付物是简要报告还是详尽研究简报？\\\\n- 2. 是否需要包含明确的检索关键词或技术点？
        
            仅返回最终的澄清问题，不输出任何额外说明或步骤
            """;

    public static final String SUMMARY_PROMPT = """
        当前用户回复：%s

        请基于以上所有对话历史和研究简报，生成一段友好且专业的总结回复，明确说明我们将为用户提供的研究报告内容与预期产出。

        要求：
        1. 开篇用1句概括研究目标与范围。
        2. 列出3-5个主要研究点或技术方向（短句）。
        3. 说明预期交付物与格式（例如：研究简报等）。
        5. 语气友好、专业、面向非技术决策者，长度控制在3-6行。
        6. 你总结的内容必须和上文的和研究简报紧密相关，不能偏离主题，不能出现研究简报中未提及的内容。

        仅返回最终的总结文本，不输出任何额外说明或步骤。
        """;

    public static final String BRIEF_PROMPT = """
        基于历史会话中用户的研究请求和澄清结果，请生成一份既面向人类阅读的研究简报（Markdown），又便于sub-agent直接调用MCP工具的结构化输出。
        
        要求输出格式：仅返回 JSON，不要返回任何额外说明，JSON 包含以下字段：
        - markdown: 字符串，研究简报的 Markdown 内容。应包含标题、1句概括、3-5个要点（短句）、当前技术趋势、预期交付物与格式，以及给非技术决策者的简短结论。
        - unknowns: 数组，每项为对象，包含：
          - id: 简短唯一标识
          - description: 模型自己认为不确定或缺失的信息（一句话）
          - impact: 如果未知项不解决会对研究结果产生的影响（一句）
          - mcp_keywords: 字符串数组，建议用于 MCP/检索的具体关键词或查询短语（尽量精确，可包含技术名词、版本、领域词）
        - assumptions: 字符串数组，列出模型基于当前会话所做的关键假设
        - recommended_actions: 数组，为sub-agent的后续执行步骤，优先级排序（可包含 "使用MCP查询" 等操作）
        
        内容规范：
        1. markdown 字段长度控制在 5-12 行，语气友好、专业，面向非技术决策者。
        2. unknowns 中每项 mcp_keywords 最少 1 个，最多 5 个关键词，便于直接用于搜索或 API 调用。
        3. 返回的 JSON 必须可被机器解析（严格的 JSON，外层不能有markdown的代码块格式包裹）。
        
        示例输出结构：
        {
          "markdown": "# ...",
          "unknowns": [
            {
              "id": "u1",
              "description": "...",
              "impact": "...",
              "mcp_keywords": ["keyword1", "keyword2"]
            }
          ],
          "assumptions": ["..."],
          "recommended_actions": ["..."]
        }
        
        仅返回 JSON，不要包含任何多余文本或解释。
        """;
}
